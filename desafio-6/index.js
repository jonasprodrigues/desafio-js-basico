// ADIÇÃO
function adicao(numero1, numero2){
    console.log('A adição dos dois números é ' + (numero1 + numero2))
}
adicao(1, 2)

// SUBTRAÇÃO
function subtracao(numero1, numero2){
    console.log('A subtração dos dois números é ' + (numero1 - numero2))
}
subtracao(1, 2)

// MULTIPLICAÇÃO
function multiplicacao(numero1, numero2){
    console.log('A multiplicação dos dois números é ' + (numero1 * numero2))
}
multiplicacao(1, 2)

// DIVISÃO
function divisao(numero1, numero2){
    console.log('A divisão dos dois números é ' + (numero1 / numero2))
}
divisao(1, 2)
